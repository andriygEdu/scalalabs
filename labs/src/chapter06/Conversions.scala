object Conversions {
  def inchesToCentimetres(inches: Double): Double =
    inches * 2.54

  def gallonsToLitres(gallons: Double) =
    gallons * 3.78541178

  def milesToKilometers(miles: Double) =
    miles * 1.609344
}
